
public class AccountClass {
    
    public String name; 
    public String accountNumber;
    public String mail;
    public String birthday;
    public String gender;
    public String phoneNumber;
    public String yearlyIncome;
    public String password;
    public String balance;
    public String electricBill;
    public String internetBill;
    public String gasBill;
    public String waterBill;
    public String govermentFee;
    public String creditCardBill;
    
    public AccountClass(String name, String accountNumber, String mail, String birthday, String gender, String phoneNumber, String yearlyIncome, String password, String balance, String electricBill, String internetBill, String gasBill, String waterBill, String govermentFee, String creditCardBill) {
        this.name = name;
        this.accountNumber = accountNumber;
        this.mail = mail;
        this.birthday = birthday;
        this.gender = gender;
        this.phoneNumber = phoneNumber;
        this.yearlyIncome = yearlyIncome;
        this.password = password;
        this.balance = balance;
        this.electricBill = electricBill;
        this.internetBill = internetBill;
        this.gasBill = gasBill;
        this.waterBill = waterBill;
        this.govermentFee = govermentFee;
        this.creditCardBill = creditCardBill;
    }
    
}
